public class VariaveisInteiras {
    public static void main(String[] args) {
        byte idade01 = 20;
        short idade02 = 20;
        int idade03 = 20;
        long idade04 = 20;

        System.out.println("Valor inteiro: " + idade01);
        System.out.println("Valor inteiro: " + idade02);
        System.out.println("Valor inteiro: " + idade03);
        System.out.println("Valor inteiro: " + idade04);
    }
}
