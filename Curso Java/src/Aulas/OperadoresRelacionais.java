public class OperadoresRelacionais {
    public static void main(String[] args) {
        int valor1 = 1;
        int valor2 = 2;

        System.out.println("Valor1 == valor2: " + (valor1 == valor2)); // sempre utilizar parênteses para fazer este tipo de operação
        System.out.println("Valor1 != valor2: " + (valor1 != valor2));
        System.out.println("Valor1 > valor2: " + (valor1 > valor2));
        System.out.println("Valor1 < valor2: " + (valor1 < valor2));
        System.out.println("Valor1 >= valor2: " + (valor1 >= valor2));
        System.out.println("Valor1 <= valor2: " + (valor1 <= valor2));
    }
}
